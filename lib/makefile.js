"use strict" ;



require( './patches.js' ) ;
var fs = require( 'fs' ) ;
var term = require( 'terminal-kit' ).terminal ;



async function summonMakefile( book , spellcastBin ) {
	var ruleList , npmInstall , makefile = '' , makefileFile = 'Makefile' ;

	// Argument management
	if ( typeof spellcastBin !== 'string' ) {
		npmInstall = true ;
		spellcastBin = './node_modules/spellcast/bin/spellcast' ;
	}


	// First, advertise...
	makefile += '\n# This Makefile has been automatically generated by Spellcast.\n# See: https://www.npmjs.org/package/spellcast\n\n' ;

	// Get all rules
	ruleList = Object.keys( book.spells ) ;

	Object.keys( book.summonings ).forEach( summoning => {
		// Do not copy summoning that share the same key as a spellcasting.
		if ( book.spells[ summoning ] || summoning === makefileFile ) { return ; }

		// Do not copy non-static summoning.
		// /!\ Some glob-summoning are possible to write in Makefile using the '%' char /!\
		if ( book.summonings[ summoning ].type !== 'static' ) { return ; }

		ruleList.push( summoning ) ;
	} ) ;

	// Build all rules
	ruleList.forEach( rule => {
		makefile += rule + ':' ;
		if ( npmInstall ) { makefile +=	' ' + spellcastBin ; }
		makefile += '\n\t' + spellcastBin + ' ' + rule + '\n\n' ;
	} ) ;

	// If necessary, add the rule to install spellcast locally
	if ( npmInstall ) {
		makefile +=	spellcastBin + ':\n\tnpm install spellcast\n\n' ;
	}

	// Make every rules 'PHONY' rules
	if ( ruleList.length ) {
		makefile +=
			"# Make every rules 'PHONY' rules, let Spellcast handle dependencies and everything else.\n" +
			'.PHONY: ' + ruleList.join( ' ' ) ;
	}

	makefile += '\n\n' ;

	try {
		await fs.writeFileAsync( makefileFile , makefile ) ;
	}
	catch ( error ) {
		term.red( 'Cannot create the file ' ).italic.bold.red( makefileFile + '\n' ) ;
		throw error ;
	}
}

module.exports = summonMakefile ;

